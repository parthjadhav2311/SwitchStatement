package SwitchStatement;

public class Switch1 {
	public static void main(String[] args){ 
		int a=10;
	switch (a)
	{
	case 10:System.out.println("Sai");
	break;
	case 20:System.out.println("Shantanu");
	break;
	case 30:System.out.println("Parth");
	break;
	default:System.out.println("Swayam");
	break;
	}
   }
}

