//Inside the switch for the case labels & switch argument


package SwitchStatement;

public class Switchargument {
	public static void main(String[] args) {
		int a = 99;
		switch(a+1)
		{
		case 10+20+70 :System.out.println("Sai");
		               break;
		case 10+5     :System.out.println("Parth");
		               break;
		case 30/6     :System.out.println("Shantanu");
		               break;
		default       :System.out.println("Swayam");
		               break;
		}
	}

}
