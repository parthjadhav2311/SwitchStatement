package SwitchStatement;

public class Switch7 {
	public static void main(String[] args) {
		int a = 10;
		switch (a){
		case 10:
			System.out.println("Parth");
			break;
		case 10 + 5:
			System.out.println("Sai");
			break;
		case 30:
			System.out.println("Avi");
			break;
			default :
				System.out.println("Rahul");
		}
	}

}
